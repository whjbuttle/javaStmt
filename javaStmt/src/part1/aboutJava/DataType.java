package part1.aboutJava;

public class DataType {
	/*
	 {, } 은 브레이스라고 부르며, {} 합쳐서는 클래스 바디(body)라고 부른다
	 현재 파일을 저장할 때는 CTRL + S
	 작업중인 모든 파일을 저장할 때는 CTRL + SHIRT + S를 한다.
	 
	 콘솔창에 보여지는 내용은 나중에 웹개발과정(JEE, JSP 과정)에서 화면(브라우저)에 보여지는 내용임
	 코딩한 내용을 콘솔에 보여지도록 하는 함수(자바에서는 메소드라고 칭함)
	 main() 메소드 단축키 : main + CTRL + SPACE + enter
	 */
	public static void main(String[] args) {
		//당분간은 별 말이 없으면 무조건 메소드 바디 내부에 코딩함
		
		// Q. 숫자 타입의 변수 num 을 생성하고 100을 할당하시오
		int num = 100;	// ; 를 구분자라고 한다
		System.out.println(num);
		// 콘솔에 결과값을 출력하는 기능의 단축키 : syso + CTRL + SPACE
		// 이 액션을 sysout 메소드의 파라미터 값으로 num을 넘겨준다
		
	}
}
